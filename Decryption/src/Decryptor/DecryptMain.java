    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        JFileChooser c = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("ENCRYPTED FILES", "encrypted");
        c.setFileFilter(filter);
        
        c.showOpenDialog(null);
        File f = c.getSelectedFile();
        
        try{
            
            enc_filename = f.getAbsolutePath();
            jTextField1.setText(enc_filename); 
        }
        catch(NullPointerException e)
        {
            jLabel2.setText("No file chosen. Please select file to encrypt... ");
        }
    
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

