    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ip = jTextField2.getText();
        //System.out.println("IP - "+ip.length());
        
        if (enc_filename.length() == 0)
            jLabel2.setText("No file choosen. Please select file to decrypt... ");
        
        else
        {
        
            if (ip.length() != 0)
            {

                DecryptOperations d = new DecryptOperations();

                try {
                    d.decrypt(enc_filename, ip);


                    jLabel2.setText("Decryption Successful. Check the file location");
                }

                catch (KeyException e) {
                     jLabel2.setText("Invalid IP");
                }
                catch (NullPointerException ex)
                {
                    jLabel2.setText("No file choosen. Please select file to decrypt... ");
                }
                catch (FileNotFoundError e)
                {
                        jLabel2.setText("No file choosen. Please select file to decrypt... ");
                }
                catch (FileNotFoundException e)
                {
                    jLabel2.setText("No file choosen. Please select file to decrypt... ");
                }
            }

            else
            {
                //System.out.println("IP - "+ip);
                jLabel2.setText("Enter Sender's IP");
            }
        }    
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
